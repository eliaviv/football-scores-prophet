class Match:
    def __init__(self, game_id, wk, day, date, time, home, xg_home, g_home, away, xg_away, g_away, league, season, score, match_link):
        self.game_id = game_id
        self.wk = wk
        self.day = day
        self.date = date
        self.time = time
        self.home = home
        self.xg_home = xg_home
        self.g_home = g_home
        self.away = away
        self.xg_away = xg_away
        self.g_away = g_away
        self.league = league
        self.season = season
        self.score = score
        self.match_link = match_link
